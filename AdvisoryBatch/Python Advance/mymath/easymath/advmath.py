'''This is module for advance mathematical computations'''
import math
def sine(x):
    '''find sine of x'''
    return math.sin(x)
def cosine(x):
    '''find cosine of x'''
    return math.cos(x)
def logT(x):
    '''find log base 10'''
    return math.log10(x)
def logE(x):
    '''find log base e'''
    return math.loge(x)