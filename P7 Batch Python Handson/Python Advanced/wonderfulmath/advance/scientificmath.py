import math
import numpy as np
INFINITI=math.inf
PIE=math.pi
E=math.e
def sine(x):
    return math.sin(x)
def cosine(x):
    return math.cos(x)
def tangent(x):
    return math.tan(x)
def mean_list(li):
    return math.mean(li)
def variance(x):
    return np.var(x)
def standard_dev(x):
    return np.std(x)
def inverse_of_x(x):
    return 1/x
def inverse_of_x2(x):
    return 1/x**2
def area_circle(r):
    return PIE*(r**2)
def peri_circle(r):
    return 2*PIE*r