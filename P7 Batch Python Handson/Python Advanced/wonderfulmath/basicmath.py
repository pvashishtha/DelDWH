def addition(x,y):
    return x+y
def subtraction(x,y):
    return x-y
def multiplication(x,y):
    return x*y
def division(x,y):
    if y==0:
        return 'Divisor Can Not be Zero!'
    else:
        return x/y
    return None
def sqr(x):
    return x**2
def cube(x):
    return x**3
