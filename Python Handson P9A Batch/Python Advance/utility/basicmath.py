def add_num(a,b):
    '''sum of two numbers'''
    return a+b
def sub_num(a,b):
    '''substraction of 2 nos'''
    return a-b
def div_num(a,b):
    '''divide a number by other'''
    if b==0:
        return 0
    return a/b
def mul_num(a,b):
    '''multiply 2 nos'''
    return a*b
def mod(a,b):
    '''finds remainder after div a by b'''
    return a%b
def expo(a,b):
    '''finds pow(a,b) using ** operator'''
    return a**b