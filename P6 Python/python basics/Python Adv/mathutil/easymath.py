def addition(x,y):
    return x+y
def substraction(x,y):
    return x-y
def remainder(x,y):
    return x%y
def whole_division(x,y):
    return x//y
def division(x,y):
    if y==0:
        return 'Divisor is Zero'
    else:
        return x/y
    return None
def multiplication(x,y):
    return x*y
def exponential(x,y):
    return x**y